  gen = Generator()
  opt2 = torch.optim.Adam(gen.parameters(),lr=0.0001)
  
  x_false=torch.rand(100,784)
  
  for i in range(n):
    print("the ",i," iteration")
    train_discriminator(opt1, disc, x_real, x_false)
    train_generator(opt2, gen, disc) 

    new_fake=torch.rand(100,100)
    new_fake=gen(new_fake)

    x_false=torch.cat((x_false,new_fake))
    x_false=x_false.detach()

    show_image(new_fake[0], "sample_%d.png"%(i), scale=SCALE_01)

