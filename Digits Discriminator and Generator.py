def train_classifier(opt, model, x, y):
   for i in range(n0):
        
        opt.zero_grad()

        y_hat=model(x)

        loss=loss_fn(y_hat,y)

        loss.backward()
        
        opt.step()

        print(i," loss is:",torch.tensor(loss))

