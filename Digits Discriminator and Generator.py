  print("TN: ", TN)
  print("FP: ", FP)
  print("FN: ", FN)

  accuracy = (TP + TN)/(TP + FP + TN + FN)
  precision = (TP)/(TP + FP)
  recall = (TP)/(TP + FN)
  F1 = (2 * precision * recall)/(precision + recall)

