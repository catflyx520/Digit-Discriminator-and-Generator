# Change Network architecture of the discriminator/classifier network. It should have 784 inputs and 1 output (0 = fake, 1 = real)
class Discriminator(nn.Module):
    def __init__(self):
        super().__init__()
        self.h=nn.Sequential(
            nn.Linear(784,512),
            nn.LeakyReLU(0.01),
            nn.Linear(512,256),
            nn.LeakyReLU(0.01),
            nn.Linear(256,1),
            nn.Sigmoid()
        )
        
    def forward(self, x):
      return self.h(x)

